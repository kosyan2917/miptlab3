import graph
import math


def elips(x1, y1, x2, r):
    el = []
    for x in range(int(x1), int(x2) + 1):
        el.append(
            (x, y1 + math.sqrt(abs(((x1 - x2) / 2)**2 / r - (x - (x1 + x2) / 2)**2 / r))))
    for x in range(int(x2), int(x1), -1):
        el.append((x, y1 - math.sqrt(abs(((x1 - x2) / 2) **
                                         2 / r - (x - (x1 + x2) / 2) ** 2 / r))))
    graph.polygon(el)


